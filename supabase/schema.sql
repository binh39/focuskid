on conflict (id) do update
set name = excluded.name,
    public = excluded.public;

create or replace function public.award_user_xp(
  target_user_id bigint,
  award_amount integer,
  award_reason text,
  award_event_key text
) returns jsonb
language plpgsql
as $$
declare
  normalized_xp integer := greatest(1, least(coalesce(award_amount, 0), 500));
  normalized_key text := left(coalesce(award_event_key, coalesce(award_reason, 'Reward') || ':' || extract(epoch from clock_timestamp())::text), 180);
  normalized_reason text := left(coalesce(award_reason, 'Reward'), 120);
  user_row public.users%rowtype;
  inserted_id bigint;
begin
  select * into user_row
  from public.users
  where id = target_user_id;

  if not found then
    return jsonb_build_object('awarded', false, 'xp', 0, 'reason', normalized_reason, 'event_key', normalized_key, 'user', null);
  end if;

  insert into public.reward_events (user_id, event_key, xp, reason)
  values (target_user_id, normalized_key, normalized_xp, normalized_reason)
  on conflict (user_id, event_key) do nothing
  returning id into inserted_id;

  if inserted_id is null then
    select * into user_row
    from public.users
    where id = target_user_id;

    return jsonb_build_object('awarded', false, 'xp', 0, 'reason', normalized_reason, 'event_key', normalized_key, 'user', to_jsonb(user_row));
  end if;

  update public.users
  set xp = coalesce(xp, 0) + normalized_xp
  where id = target_user_id
  returning * into user_row;

  return jsonb_build_object('awarded', true, 'xp', normalized_xp, 'reason', normalized_reason, 'event_key', normalized_key, 'user', to_jsonb(user_row));
end;
$$;

-- Focus session history: records each reading/focus session as metadata only.
create table if not exists public.focus_sessions (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.users(id) on delete cascade,
  mission_id bigint references public.missions(id) on delete set null,
  file_id bigint references public.mission_files(id) on delete set null,
  planned_minutes integer not null default 15,
  started_at timestamptz not null default now(),
  ended_at timestamptz,
  completed boolean not null default false,
  distraction_count integer not null default 0
);

create index if not exists idx_focus_sessions_user_id on public.focus_sessions (user_id);
create index if not exists idx_focus_sessions_started_at on public.focus_sessions (started_at desc);

-- Distraction events: stores attention drift metadata, never camera/video data.
create table if not exists public.distraction_events (
  id bigint generated by default as identity primary key,
  session_id bigint references public.focus_sessions(id) on delete cascade,
  user_id bigint not null references public.users(id) on delete cascade,
  mission_id bigint references public.missions(id) on delete set null,
  file_id bigint references public.mission_files(id) on delete set null,
  reason text not null default 'attention_drift',
  created_at timestamptz not null default now()
);

create index if not exists idx_distraction_events_session_id on public.distraction_events (session_id);
create index if not exists idx_distraction_events_user_id on public.distraction_events (user_id);
create index if not exists idx_distraction_events_created_at on public.distraction_events (created_at desc);
