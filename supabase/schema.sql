-- FocusKid Supabase schema
-- Run this in the Supabase SQL editor.

create table if not exists public.users (
  id bigint generated by default as identity primary key,
  name text not null,
  role text not null,
  email text not null,
  password_hash text,
  parent_id bigint references public.users(id) on delete set null,
  xp integer not null default 0,
  created_at timestamptz not null default now()
);

-- Make email unique per role and add parent_id for child accounts.
alter table public.users add column if not exists password_hash text;
alter table public.users add column if not exists parent_id bigint references public.users(id) on delete set null;
create unique index if not exists idx_users_email_role on public.users (email, role);
create index if not exists idx_users_parent_id on public.users (parent_id);

create table if not exists public.missions (
  id bigint generated by default as identity primary key,
  title text not null,
  time_minutes integer,
  parent_id bigint references public.users(id) on delete set null,
  file_path text,
  file_storage_path text,
  created_at timestamptz not null default now()
);

create table if not exists public.subtasks (
  id bigint generated by default as identity primary key,
  mission_id bigint not null references public.missions(id) on delete cascade,
  title text not null,
  completed boolean not null default false,
  created_at timestamptz not null default now()
);

create table if not exists public.mission_files (
  id bigint generated by default as identity primary key,
  mission_id bigint not null references public.missions(id) on delete cascade,
  storage_path text not null,
  file_path text not null,
  original_name text not null,
  time_minutes integer,
  completed boolean not null default false,
  created_at timestamptz not null default now()
);

create table if not exists public.mission_quizzes (
  id bigint generated by default as identity primary key,
  mission_id bigint not null references public.missions(id) on delete cascade,
  question text not null,
  option_a text not null,
  option_b text not null,
  option_c text not null,
  option_d text not null,
  correct_option text not null check (correct_option in ('A', 'B', 'C', 'D')),
  selected_option text,
  completed boolean not null default false,
  created_at timestamptz not null default now()
);

create table if not exists public.reward_events (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.users(id) on delete cascade,
  event_key text not null,
  xp integer not null,
  reason text not null,
  created_at timestamptz not null default now(),
  unique (user_id, event_key)
);

create index if not exists idx_subtasks_mission_id on public.subtasks (mission_id);
create index if not exists idx_mission_files_mission_id on public.mission_files (mission_id);
create index if not exists idx_mission_quizzes_mission_id on public.mission_quizzes (mission_id);
create index if not exists idx_reward_events_user_id on public.reward_events (user_id);
create index if not exists idx_missions_created_at on public.missions (created_at desc);

insert into storage.buckets (id, name, public)
